package techguardian.api.dto;

public class OutputDTO {

    private String dataSaida;
    private String horaSaida;
    private int quantSaida;
    private String obsSaida;
    
    public String getDataSaida() {
        return dataSaida;
    }
    public void setDataSaida(String dataSaida) {
        this.dataSaida = dataSaida;
    }
    public String getHoraSaida() {
        return horaSaida;
    }
    public void setHoraSaida(String horaSaida) {
        this.horaSaida = horaSaida;
    }
    public int getQuantSaida() {
        return quantSaida;
    }
    public void setQuantSaida(int quantSaida) {
        this.quantSaida = quantSaida;
    }
    public String getObsSaida() {
        return obsSaida;
    }
    public void setObsSaida(String obsSaida) {
        this.obsSaida = obsSaida;
    }

    
}
